package com.silentmatt.dss.expression;

/**
 * Specifies the operation for a {@link BinaryExpression}.
 *
 * @author Matthew Crumley
 */
public enum Operation {
    /**
     * Addition
     */
    Add,
    /**
     * Subtraction
     */
    Subtract,
    /**
     * Multiplication
     */
    Multiply,
    /**
     * Division
     */
    Divide
}
